# Operadores Aritméticos
# adicao +
print('adicao 4+6 =', 4+6)
# subtracao -
print('subtracao 10-20 =', 10-20)
# multiplicacao *
print('multiplicacao 6*7 =', 6*7)
#  divisão / [retorna float, mesmo entre inteiros]
print('divisao comum 10/2 =', 10/2)
# divião com retorno apenas da parte inteira, ou seja, se quiser apenas um 5 como resposta da divisão 10/5, em vez de 5.0, faça 10//2
print('divisao com retorno somente da parte inteira 10//3 =', 10//3)
# retorno do resto da divisão
print('resto da divisão 10%3 =', 10%3)
