# Funçoes Built-In

# Função abs() retorna o valor absoluto do número
print('abs(-9) retorna:', abs(-9))

# min() retorna o valor mínimo de uma sequencia
print('min(6,7,8,9,10), resultado:', min(6,7,8,9,10))

# max() retorna o valor máximo de uma sequencia
print('max(6,7,8,9,10), resultado:', max(6,7,8,9,10))

# sum() faz a soma dos valores de uma sequencia
# somaTotal = sum([2, 3, 5])
somaTotal = sum([2, 3, 5])
print('somaTotal = sum([2, 3, 5]) retorna:', somaTotal)

# pow() exponenciacao
print('pow(2,3) (ou 2**3) =', pow(2,3))

# a função print mostra um valor na tela
print('print()')

# input() lê um valor digitado no teclado pelo usuário
# lembre-se que um input retorna sempre uma string
nome = input('Qual é o seu nome?')


# help() mostra as funcionalidades de uma determinada função
help(max)


