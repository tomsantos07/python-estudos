# Estruturas de repetição [While]

varNumero = 0
while varNumero <= 10:
    print(varNumero)
    varNumero += 1
print('Acabou o loop')

varMaxNumber = eval(input('Digite um número:'))
varInitNumber = 0

while varInitNumber <= varMaxNumber:
    print(varInitNumber)
    varInitNumber += 1
print('Fim do Loop.')
